--------------------------------------------------------------------------------
--  Problem 7
--------------------------------------------------------------------------------
--  What is the 10001st prime number?
--------------------------------------------------------------------------------
--  104743
--------------------------------------------------------------------------------

module Problem007 where

import Juke (primes)

solutionFrom [] = solutionFrom ["10001"]
solutionFrom [nS] = return $ show $ solutionGen (read nS)

solutionGen n = primes !! (n - 1)
